#VARIABLES:
#stores a value
x = 5 #x HOLDS 5, not x equals 5
#the value 5 is an int
print(x)
#we can check the data type of x
print(type(x))
#we can reassign x
x = 5.5
#this is a float datatype (has a fractional part)
print(x)
print(type(x))
#we can also use strings
x = "heyo"
print(x)
print(type(x))


#OPERATORS
#python follows PEMDAS (and more)
# +, -, 
# * is multiplication
print(4 * 5)
# / is floating point division (like normal division)
print(5 / 2)
# // is integer devision (the whole number version of /)
print(5 // 2)
# % is the modulus operator (gives remainder of integer division)
print(5 % 2)
# ** is the exponent operator (power)
print(5 ** 2)
